package com.rnapp.rnchatview

// ─────────────────────────────────────────────────────────────────────────────
// InputBarContract.kt
//
// Зона ответственности: публичный контракт инпут-бара.
// Здесь живут:
//   • InputBarDelegate — интерфейс для передачи событий наружу (в RNChatView)
//   • InputBarMode     — sealed-класс, описывающий три состояния инпут-бара
//
// Ничего UI-специфичного здесь нет — файл можно читать как «спецификацию».
// ─────────────────────────────────────────────────────────────────────────────

/**
 * Делегат инпут-бара.
 * RNChatView реализует этот интерфейс и получает уведомления о всех действиях
 * пользователя: отправка, редактирование, отмена, изменение высоты.
 */
interface InputBarDelegate {

    /**
     * Пользователь нажал «Отправить» в обычном режиме или в режиме ответа.
     * @param text      введённый текст (уже trimmed)
     * @param replyToId id сообщения-цитаты, либо null если ответа нет
     */
    fun onSendText(text: String, replyToId: String?)

    /**
     * Пользователь нажал «Отправить» в режиме редактирования существующего сообщения.
     * @param text      новый текст сообщения
     * @param messageId id редактируемого сообщения
     */
    fun onEditText(text: String, messageId: String)

    /** Пользователь закрыл панель ответа (крестик или отправка). */
    fun onCancelReply()

    /** Пользователь закрыл панель редактирования (крестик или отправка). */
    fun onCancelEdit()

    /** Пользователь нажал кнопку прикрепления файла. */
    fun onAttachmentPress()

    /**
     * Высота инпут-бара изменилась — вызывается на каждом кадре анимации.
     * RNChatView использует это чтобы синхронно двигать RecyclerView вверх/вниз
     * и не допускать «прыжков» списка при открытии/закрытии панели.
     *
     * @param heightPx              полная высота инпут-бара в пикселях
     * @param topPanelVisibleHeight сколько пикселей верхней панели сейчас видно (0..panelHeight)
     */
    fun onHeightChanged(heightPx: Int, topPanelVisibleHeight: Int)
}

// ─────────────────────────────────────────────────────────────────────────────

/**
 * Режим инпут-бара. Определяет, что именно показывает верхняя панель
 * и что произойдёт при нажатии «Отправить».
 */
sealed class InputBarMode {

    /** Обычный режим — верхняя панель скрыта, отправка создаёт новое сообщение. */
    object Normal : InputBarMode()

    /**
     * Режим ответа на сообщение.
     * [info] содержит снапшот данных цитируемого сообщения для отображения в панели.
     */
    data class Reply(val info: ReplyInfo) : InputBarMode()

    /**
     * Режим редактирования сообщения.
     * [messageId]    — id сообщения, которое редактируем
     * [originalText] — исходный текст, подставляется в EditText при входе в режим
     */
    data class Edit(val messageId: String, val originalText: String) : InputBarMode()
}
